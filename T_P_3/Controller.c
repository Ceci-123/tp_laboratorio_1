{
    int todoOk= 0;
    FILE* f = fopen(path, "wb");
    int tamanio = ll_len(pArrayListEmployee);

     if(f == NULL)
     {
         printf("No se pudo abrir el archivo\n");
         exit(1);
     }
     for(int i= 0; i < tamanio; i++)
     {
        if((pArrayListEmployee + i) != NULL)
        {
           if(fwrite((pArrayListEmployee + i), sizeof(Employee), 1, f) < 1)
           {
               break;
           }
        }
     }
    fclose(f);
    todoOk = 1;

    return todoOk;
}

